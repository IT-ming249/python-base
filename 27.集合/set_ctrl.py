#集合的常见操作
print('集合元素的判断操作')#使用in/not in
s={10,20,30,40,50}
print(10 in s)
print(10 not in s)
print('---------')
print('集合元素的添加操作')
s={10,20,30,40,50}
#方法一:使用add()函数添加一个元素
s.add(100)
print(s)
#方法二：使用upda()函数至少添加一个元素
s={10,20,30,40,50}
s.update({200,300,400})#update的参数是可迭代对象，包括集合本身、列表、元组、字典。字典update键值
print(s)
s.update([11,12,13,14])
print(s)
s.update((1,2,3,))
print(s)
print('-------------')
print('集合元素的删除操作')
#方法一：使用remove()函数，一次删除一个指定元素
s={10,20,30,40,50}
s.remove(10)#s.remove(100)该元素不存在就报错
print(s)
#方法二：使用disca（）函数，一次删除一个指定元素
s={10,20,30,40,50}
s.discard(20)#要是有就删，没有也不报错
s.discard(500)
print(s)
#方法三：使用pop()函数，随机删除一个元素
s={10,20,30,40,50}
s.pop()
s.pop()#s.pop()不能指定参数,会报错
print(s)
#清空集合
s={10,20,30,40,50}
s.clear()
print(s)