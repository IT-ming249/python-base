#集合：与列表，字典一样是可变序列，集合可以理解为没有值的字典
print('集合的创建方式')
#方法一：使用{}
s={1,2,2,3,4,5,5}#集合中的元素不允许重复，集合满足：1.确定性，2.互异性 ，3.无序性
print(s,type(s))
print('-------')
#方法二：使用内置函数set()
s1=set(range(6))
print(s1,type(s1))
s2=set([1,2,2,3,4,5,5])#列表转集合
print(s2,type(s2))#列表转成了集合，同时去掉了重复元素
s3=set((1,2,2,45,33,5))#元组转集合
print(s3,type(s3))#元组转成了集合，同时去掉了重复值，也可以看到元素位置发生了改变，说明集合是无序的
s4=set('Python')
print(s4,type(s4))#集合是无序的
#空集合
s5={}#这样变成了字典
print(s5,type(s5))
s6=set()#这样才是空集合
print(s6,type(s6))