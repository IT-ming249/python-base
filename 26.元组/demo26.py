#元组：Python的内置数据结构之一，是一个不可变序列
#关于可变序列与不可变序列的区别：
#不可变序列：整数，字符串，元组，即不能进行增删改查操作
#可变序列：列表，字典，即可以进行增删改查操作，对象地址不会发生改变
print('元组创建方式')
#方法一：用小括号，
t=('python','word',98)
print(t,type(t))
t2='python','word',98 #省略小括号也能创建元组
print(t2,type(t2))
t3=(10,)#只包含一个元素的元组，需要同时使用小括号和逗号
print(t3,type(t3))
#方法二：使用内置函数tuple()
t1=tuple(('hello','word'))#tuple()函数只能输入一个参数
print(t1,type(t1))
#空元组
t4=()
print(t4,type(t4))
print('-----------')
#注意：
#元组存储的是对象的引用
#1）如果元组中的对象是不可变对象，则不能再引用其它对象
#2）如果元组中的对象是可变对象，则可变对象的引用不允许改变，但数据可以改变
t5=(10,[20,25],30)
print(t5,type(t5))
#获取元组数据，可以通过索引的方式获取
print(t5[0],type(t5[0]),id(t5[0]))
print(t5[1],type(t5[1]),id(t5[1]))
print(t5[2],type(t5[2]),id(t5[2]))
#尝试修改t5[1]
#t[1]=100 #尝试把元组中的列表改成整数，原来指向列表，现在指向整数，即变更它的对象的引用，这样就会报错
#由于列表是可变序列，可以对列表中的元素进行修改，而列表的内存地址不能变
t5[1].append(100)
print(t5,id(t5[1]))#可以发现列表的地址没有改变
